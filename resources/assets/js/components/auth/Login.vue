<template>
    <div class="login-form">
        <form @submit.prevent="authenticate">
            <h2 class="text-center">Ingreso al Sistema</h2>   
            <div class="form-group">
                <div class="input-group">
                    <span class="input-group-addon"><i class="fa fa-user"></i></span>
                    <input v-model="form.email" class="form-control" placeholder="Correo Electronico" required="required" type="text">				
                </div>
            </div>
            <div class="form-group">
                <div class="input-group">
                    <span class="input-group-addon"><i class="fa fa-lock"></i></span>
                    <input v-model="form.password" class="form-control" placeholder="ContraseÃ±a" required="required" type="password">				
                </div>
            </div>        
            <div class="form-group">
                <button type="submit" class="btn btn-primary login-btn btn-block">Ingresar</button>
            </div>
            <div class="form-group row" v-if="authError">
                <p class="error">
                    {{ authError }}
                </p>
            </div>        
        </form>
    </div>
</template>

<script>
    import {login} from '../../helpers/auth';

    export default {
        name: "login",
        data() {
            return {
                form: {
                    email: '',
                    password: ''
                },
                error: null
            };
        },
        methods: {
            authenticate() {
                this.$store.dispatch('login');

                login(this.$data.form)
                    .then((res) => {
                        this.$store.commit("loginSuccess", res);
                        this.$router.push({path: '/'});
                    })
                    .catch((error) => {
                        this.$store.commit("loginFailed", {error});
                    });
            }
        },
        computed: {
            authError() {
                return this.$store.getters.authError;
            }
        }
    }
</script>

